#Dados dois números inteiros, qual é o resto e o quociente da divisão do primeiro pelo segundo?
#Exercicio 2 

numero_1 = int(input(' digite um numero '))
numero_2 = int(input(' digite um numero '))
resto = numero_1 % numero_2
quociente = numero_1 / numero_2
print('o resto é', resto)
print('o quociente', quociente)